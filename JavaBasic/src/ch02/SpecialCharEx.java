package ch02;

public class SpecialCharEx {

	public static void main(String[] args) {

		System.out.println('\''); // '
		System.out.println("abc\t123\b456"); // abc	123456 : \b (backspace)에 의해 3이 지워진다 
		System.out.println('\n'); // 개행(new line) 문자 출력하고 개행
		System.out.println("\"Hello\""); // "Hello" 큰 따옴표를 출력하는 방법
		System.out.println("c:\\"); // c:\
		
	}
}
