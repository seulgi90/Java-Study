package ch04;

import java.util.Scanner;

public class FlowEx12 {

	public static void main(String[] args) {

		for (int i =1; i<=5; i++) System.out.println(i);
		// 1
		// 2
		// 3
		// 4
		// 5
		for (int i =1; i<=5; i++) System.out.print(i); // print()를 쓰면 가로로 출력
		// 12345
		System.out.println();
	}
}
